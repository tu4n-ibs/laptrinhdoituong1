public class variableandata {
    public static void main(String[] args) {

        int a = 8;
        System.out.println("a = " + a);

        double b = 8.0;
        System.out.println("b = " + b);

        float c = 80.5f;
        System.out.println("c = " + c);

        boolean d = true;
        System.out.println("d = " + d);

        char e = 'q';
        System.out.println("e = " + e);

        String h = "ha noi";
        System.out.println("h = " + h);
    }
}
